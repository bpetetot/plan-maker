// Generates src/transfer/measureFont.ts: the measure-glyph subset of
// JetBrains Mono 400 as a woff2 data URI, embedded by the PNG export
// (the standalone SVG loads no external resource).
// Run: node scripts/generate-measure-font.mjs
import { readFileSync, writeFileSync } from 'node:fs'
import subsetFont from 'subset-font'

// Every character formatLength/formatArea can emit: digits, decimal comma,
// space, "cm", "m", "m²".
const MEASURE_CHARS = '0123456789, cm²'

const source = readFileSync(
  'node_modules/@fontsource/jetbrains-mono/files/jetbrains-mono-latin-400-normal.woff2',
)
const subset = await subsetFont(source, MEASURE_CHARS, { targetFormat: 'woff2' })

const banner = `// Generated by scripts/generate-measure-font.mjs — do not edit.
// JetBrains Mono 400 (OFL) subset to the measure glyphs ("${MEASURE_CHARS}"),
// embedded by the PNG export as a data URI.
`
writeFileSync(
  'src/transfer/measureFont.ts',
  `${banner}export const MEASURE_FONT_DATA_URI =\n  'data:font/woff2;base64,${subset.toString('base64')}'\n`,
)
console.log(`measureFont.ts written (${subset.length} bytes of woff2)`)
